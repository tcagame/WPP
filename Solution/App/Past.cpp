GRAPH Past::getGraph( ) const {
	return GRAPH_SCREEN_PAST;
}

bool Past::isExistance( Vector pos ) const {
	int idx = (int)( pos.y - 0.5 ) * DOT_NUM + (int)pos.x;
	if ( DATA[ idx ] == '*' ) {
		return true;
	}
	return false;
}
